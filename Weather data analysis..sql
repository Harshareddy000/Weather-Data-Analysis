CREATE database weather_data;
use weather_data;
-- Create Location table
CREATE TABLE Location (
    LocationID INT PRIMARY KEY,
    City VARCHAR(100),
    Country VARCHAR(100)
);

desc Location;
-- Create WeatherData table
CREATE TABLE WeatherData (
    WeatherID INT PRIMARY KEY,
    LocationID INT,
    ObservationDate DATE,
    Temperature FLOAT,
    Precipitation FLOAT,
    WindSpeed FLOAT,
    Humidity FLOAT,
    CONSTRAINT fk_Location FOREIGN KEY (LocationID) REFERENCES Location(LocationID)
);

desc WeatherData;
-- Create WeatherForecast table
CREATE TABLE WeatherForecast (
    ForecastID INT PRIMARY KEY,
    LocationID INT,
    ForecastDate DATE,
    ForecastTemperature FLOAT,
    ForecastPrecipitation FLOAT,
    ForecastWindSpeed FLOAT,
    ForecastHumidity FLOAT,
    CONSTRAINT fk_Location_Forecast FOREIGN KEY (LocationID) REFERENCES Location(LocationID)
);

desc WeatherForecast;
-- Create WeatherEvents table
CREATE TABLE WeatherEvents (
    EventID INT PRIMARY KEY,
    LocationID INT,
    EventDate DATE,
    EventType VARCHAR(100),
    EventDescription TEXT,
    CONSTRAINT fk_Location_Events FOREIGN KEY (LocationID) REFERENCES Location(LocationID)
);

desc WeatherEvents;
-- Create WeatherStation table
CREATE TABLE WeatherStation (
    StationID INT PRIMARY KEY,
    LocationID INT,
    StationName VARCHAR(100),
    Latitude FLOAT,
    Longitude FLOAT,
    CONSTRAINT fk_Location_Station FOREIGN KEY (LocationID) REFERENCES Location(LocationID)
);

desc WeatherStation;
-- Create WeatherAlerts table
CREATE TABLE WeatherAlerts (
    AlertID INT PRIMARY KEY,
    LocationID INT,
    AlertDate DATE,
    AlertType VARCHAR(100),
    AlertDescription TEXT,
    CONSTRAINT fk_Location_Alerts FOREIGN KEY (LocationID) REFERENCES Location(LocationID)
);

desc WeatherAlerts;

